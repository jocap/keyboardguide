	<h3>History</h3>

	<p><b style="font-weight:500">KeyboardGuide</b> was created in 2014 by mechanical keyboard enthusiast John Ankarstr√∂m, from Sweden.  After having, on multiple occasions, spilled Coca-Cola on his trusty QPAD MK-85, and attaching his broken wrist rest with tape, he started looking for a new keyboard.

	<p>He searched the interwebs for a website to guide him through the world of mechanical switches, but alas, none he found.  He decided there needed to be one.  Thus, KeyboardGuide was born.

	<h3>What is KeyboardGuide?</h3>

	<p>KeyboardGuide is a tool designed to find the perfect mechanical keyboard for <em>you</em>.  Answer a bunch of questions about what keyboard you would like to purchase, and it returns a bunch of suitable keyboards.  It can also list keyboards based on other keyboards or switches that you like.

	<p>KeyboardGuide is a collaborative tool, which means that it is up to the community to <a href="<?=url('add/')?>">add new keyboards</a>.

	<h3>GitHub Repository</h3>

